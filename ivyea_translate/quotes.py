class Deck:
    """洗牌发牌：一轮之内不重复，发完再洗。

    纯随机会连着抽到同一句（生日悖论），一百多条里连抽两次相同的概率不低，
    用户一眼就看出来"这轮播坏了"。
    """

    def __init__(self, items=None, seed: int | None = None):
        self._items = list(items if items is not None else QUOTES)
        self._rng = random.Random(seed)
        self._pool: List[Tuple[str, str]] = []

    def draw(self) -> Tuple[str, str]:
        if not self._pool:
            self._pool = list(self._items)
            self._rng.shuffle(self._pool)
        return self._pool.pop()
